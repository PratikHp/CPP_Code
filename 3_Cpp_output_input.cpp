#include<iostream>
using namespace std;

int main(){
    int num1, num2;

    cout<<"Enter the value of num1 ";//'<<' This is called Insertion Operator
    cin>>num1;//'>>' This is called Extractor Operator

    cout<<"Enter the value of num2 ";//'<<' This is called Insertion Operator
    cin>>num2;//'>>' This is called Extractor Operator

    cout<<"The sum is "<<num1+num2;
    return 0;
}